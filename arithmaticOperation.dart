// Arithmatic operations in Dart
void main() {
  int a = 10;
  int b = 5;

  // Addition
  int sum = a + b;
  print('Sum: $sum'); // Outputs: Sum: 15

  // Subtraction
  int difference = a - b;
  print('Difference: $difference'); // Outputs: Difference: 5

  // Multiplication
  int product = a * b;
  print('Product: $product'); // Outputs: Product: 50

  // Division
  double quotient = a / b;
  print('Quotient: $quotient'); // Outputs: Quotient: 2.0

  // Modulus
  int remainder = a % b;
  print('Remainder: $remainder'); // Outputs: Remainder: 0

  // Increment
  a++;
  print('Incremented a: $a'); // Outputs: Incremented a: 11

  // Decrement
  b--;
  print('Decremented b: $b'); // Outputs: Decremented b: 4
}
